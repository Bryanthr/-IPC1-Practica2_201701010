    public void productos(){
        DefaultTableModel modeloProductos = (DefaultTableModel) ListadoProductos.getModel();
        modeloProductos.setValueAt("Pizza Grande Especialidad", 0, 0);
        modeloProductos.setValueAt("Pizza Grande 1 Ingrediente", 1, 0);
        modeloProductos.setValueAt("Pizza Mediana 1 Ingrediente", 2, 0);
        modeloProductos.setValueAt("Pizza Pesonal 1 Ingrediente", 3, 0);
        modeloProductos.setValueAt( "Q 120", 0, 1);
        modeloProductos.setValueAt("Q 100", 1, 1);
        modeloProductos.setValueAt( "Q 50", 2, 1);
        modeloProductos.setValueAt("Q 35", 3, 1);
    }
    
    public void Pedido(){
        DefaultTableModel modeloPedido = (DefaultTableModel) ListadoPedidos.getModel();
        modeloPedido.addRow(new Object[]{producto, precio});
        
        // Calcular el total sumando los precios de los productos en la tabla ListadoPedidos
        int total = 0;
        for (int i = 0; i < modeloPedido.getRowCount(); i++) {
            String precioProductoStr = modeloPedido.getValueAt(i, 1).toString();
            int precioProducto = Integer.parseInt(precioProductoStr.substring(2)); // Eliminar el símbolo de moneda "Q" y convertir a entero
            total += precioProducto;
        }
        // Actualizar el label Totallbl con el total calculado
        Totallbl.setText(String.valueOf(total));
    }
    
    
    
    
    
    

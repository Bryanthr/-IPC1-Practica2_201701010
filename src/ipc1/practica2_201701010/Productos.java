    private void AgregarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarBtnActionPerformed
        fila = ListadoProductos.getSelectedRow();
        
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un producto de la tabla", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Salir del método sin agregar el producto al pedido
        }else{
            producto = ListadoProductos.getModel().getValueAt(fila, 0).toString();
            precio = ListadoProductos.getModel().getValueAt(fila, 1).toString();
        
            Pedido();
        }
        
    }//GEN-LAST:event_AgregarBtnActionPerformed

    
    private void ConfiramrBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfiramrBtnActionPerformed
        // TODO add your handling code here:
        
        LocalDateTime fechaHora = LocalDateTime.now();
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy - HH:mm:ss");
        fechaHoraFormateada = fechaHora.format(formato);
        
        montoTotal = Integer.parseInt(Totallbl.getText());
        vehiculoSeleccionado = VehiculoBox.getSelectedItem().toString();
        kilometros = Integer.parseInt(KilometrosTxt.getText());

        // Obtener la fecha y hora actual
        LocalDateTime fechaHoraActual = LocalDateTime.now();

        // Agregar la cantidad de minutos según la selección del ComboBox
        int minutosExtra = 0;
        if (vehiculoSeleccionado.equals("Motocicleta")) {
            minutosExtra = 10;
        } else if (vehiculoSeleccionado.equals("Automóvil")) {
            minutosExtra = 20;
        } else if (vehiculoSeleccionado.equals("Bicicleta")) {
            minutosExtra = 30;
        }

        LocalDateTime fechaHoraEntrega = fechaHoraActual.plusMinutes(minutosExtra + (kilometros*2));

        // Formatear la fecha y hora de entrega
        fechaHoraEntregaFormateada = fechaHoraEntrega.format(formato);

        // Aquí puedes hacer lo que necesites con las variables (ejemplo: imprimir, almacenar en una base de datos, etc.)
        System.out.println("Monto Total: " + montoTotal);
        System.out.println("Vehículo seleccionado: " + vehiculoSeleccionado);
        System.out.println("Kilómetros: " + kilometros);
        System.out.println("Fecha y Hora: " + fechaHoraFormateada);
        System.out.println("Fecha y Hora de Entrega: " + fechaHoraEntregaFormateada);
        
        // Eliminar todos los datos de la tabla
        DefaultTableModel modelo = (DefaultTableModel) ListadoPedidos.getModel();
        modelo.setRowCount(0);
        
    }//GEN-LAST:event_ConfiramrBtnActionPerformed

